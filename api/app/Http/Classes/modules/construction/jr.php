  public function loaddoclisting($config)
  {
    $date1 = date('Y-m-d', strtotime($config['params']['date1']));
    $date2 = date('Y-m-d', strtotime($config['params']['date2']));
    $itemfilter = $config['params']['itemfilter'];
    $doc = $config['params']['doc'];
    $center = $config['params']['center'];
    $condition = '';
    $searchfilter = $config['params']['search'];
    $projectfilter = "";

    $project = $this->coreFunctions->getfieldvalue("useraccess", "project", "username=?", [$config['params']['user']]);
    $projectid = $this->coreFunctions->getfieldvalue("projectmasterfile", "line", "code=?", [$project]);

    $viewall = $this->othersClass->checkAccess($config['params']['user'], 2445);

    $limit = "limit 150";


    if ($viewall == '0') {
      $projectfilter = " and head.projectid = " . $projectid . " ";
    }
    switch ($itemfilter) {
      case 'draft':
        $condition = ' and num.postdate is null ';
        break;
      case 'posted':
        $condition = ' and num.postdate is not null ';
        break;
    }
    $filtersearch = "";
    if (isset($config['params']['search'])) {
      $searchfield = ['head.docno', 'head.clientname', 'p.name', 'num.postedby', 'head.createby', 'head.editby', 'head.viewby'];
      if ($config['params']['companyid'] == 8) array_push($searchfield, 'head.rem');
      $search = $config['params']['search'];
      if ($search != "") {
        $filtersearch = $this->othersClass->multisearch($searchfield, $search);
      }
      $limit = "";
    }

    $qry = "select head.trno,head.docno,p.name as projectname,head.clientname,left(head.dateid,10) as dateid, 'DRAFT' as status,head.rem,head.createby,head.editby,head.viewby,num.postedby  
     from " . $this->head . " as head left join " . $this->tablenum . " as num 
     on num.trno=head.trno left join projectmasterfile as p on p.line = head.projectid where head.doc=? and num.center=? and CONVERT(head.dateid,DATE)>=? and CONVERT(head.dateid,DATE)<=? " . $condition . $projectfilter . " " . $filtersearch . "
     union all
     select head.trno,head.docno,p.name as projectname,head.clientname,left(head.dateid,10) as dateid,'POSTED' as status,head.rem,head.createby,head.editby,head.viewby, num.postedby  
